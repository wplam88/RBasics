# vector operations practice

# adding vectors of equal length
c(5, 10, 15) + c(0, 5, 10)

# concatenate 2 vectors
texts = c("first", "second", "third")
texts2 = c("1st", "2nd", "3rd")

paste(texts, texts2)

# vector recycling
one_word = "string"

# concatenate 2 vectors of unequal length
paste(texts2, one_word)



